package heroe;

public class Ayudante {
	
	String nombre, origen;

	public Ayudante(String nombre, String origen) {
		super();
		this.nombre = nombre;
		this.origen = origen;
	}

	public String getNombre() {
		return this.nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getOrigen() {
		return this.origen;
	}

	public void setOrigen(String origen) {
		this.origen = origen;
	}
	

}
